import java.util.Scanner;

public class justNextTheGame_CF {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while (t-->0)
        {
            int n = sc.nextInt();
            int alice[] = new int[n];
            int bob[] = new int[n];
            for (int i = 0; i < n; i++) {
                if (alice[i]-bob[i]==-1)
                    continue;


            }

        }
    }
}
